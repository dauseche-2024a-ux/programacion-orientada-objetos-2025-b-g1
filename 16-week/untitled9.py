class Vehicle(ABC):
    """Clase abstracta que define la estructura general de un veh铆culo."""
    def __init__(self, plate: str, capacity: int):
        self._plate = plate
        self._capacity = capacity
        self._odometer = 0
        self._assigned_driver = None

    @property
    def plate(self):
        return self._plate

    @property
    def capacity(self):
        return self._capacity

    @property
    def odometer(self):
        return self._odometer

    def _increase_odometer(self, km: int):
        if km > 0:
            self._odometer += km

    def assign_driver(self, driver):
        self._assigned_driver = driver

    def unassign_driver(self):
        self._assigned_driver = None

    @property
    def assigned_driver(self):
        return self._assigned_driver

    @abstractmethod
    def start_engine(self):
        pass

    @abstractmethod
    def drive(self, km: int):
        pass

    @abstractmethod
    def needs_maintenance(self) -> bool:
        pass

    @abstractmethod
    def vehicle_type(self) -> str:
        pass


class LandVehicle(Vehicle):
    def __init__(self, plate: str, capacity: int, wheels: int):
        super().__init__(plate, capacity)
        self._wheels = wheels

    @property
    def wheels(self):
        return self._wheels

    def vehicle_type(self) -> str:
        return f"LandVehicle ({self._wheels} wheels)"


